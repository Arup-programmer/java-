public class exercise3 {
//    write a java program to print the sum,multiply,subtract,divide and remainder of two numbers.
public static void main(String[] args) {
    int num1 = 125;
    int num2 = 24;
    System.out.println("The results are below");
    System.out.println("\n");
    System.out.println("add two numbers 125 + 24 =");
    System.out.println(num1+num2);
    System.out.println("multiply 2 numbers 125 * 24 =");
    System.out.println(num1*num2);
    System.out.println("subtract 2 numbers 125 - 24 =");
    System.out.println(num1-num2);
    System.out.println("division of 2 number 125 / 24 =");
    System.out.println(num1/num2);
    System.out.println("remainder of two numbers 125 % 24 =");
    System.out.println(num1%num2);
}
}
